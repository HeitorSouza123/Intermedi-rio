dia_semana = int(input("Digite um número de 1 a 7: "))
if dia_semana == 1:
    print("Dia 1: Domingo")
elif dia_semana == 2:
    print("Dia 2: Segunda")
elif dia_semana == 3:
    print("Dia 3: Terça")
elif dia_semana == 4:
    print("Dia 4: Quarta")
elif dia_semana == 5:
    print("Dia 5: Quinta")
elif dia_semana == 6:
    print("Dia 6: Sexta")
elif dia_semana == 7:
    print("Dia 7: Sábado")
else:
    print("Número inválido!")